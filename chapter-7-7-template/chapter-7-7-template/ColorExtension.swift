import SwiftUI

extension Color {
    
    static var brightOrange: Color {
        return Color(UIColor(red: 1, green: 0.63, blue: 0.18, alpha: 1))
    }
    
    static var pureOrange: Color {
        return Color(UIColor(red: 0.87, green: 0.40, blue: 0, alpha: 1))
    }
    
    static var grayishOrange: Color {
        return Color(UIColor(red: 0.92, green: 0.85, blue: 0.78, alpha: 1))
    }
    
    static var darkCyan: Color {
        return Color(UIColor(red: 0, green: 0.48, blue: 0.48, alpha: 1))
    }
    
    static var darkBlue: Color {
        return Color(UIColor(red: 0, green: 0.25, blue: 0.35, alpha: 1))
    }
    
}
